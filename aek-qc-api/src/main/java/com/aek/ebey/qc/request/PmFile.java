package com.aek.ebey.qc.request;

public class PmFile {
	/**
	 * 文件名称
	 */
	private String name;
	/**
	 * 文件路径
	 */
	private String url;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
